                            <p>La Commission de la Communauté économique des États de l’Afrique de l’Ouest (Commission
                                de la CEDEAO), qui avait le statut d’un Secrétariat jusqu’en 2007, est une organisation
                                régionale regroupant quinze (15) États membres. Elle est chargée de la mise en œuvre de
                                la vision, de la mission et des objectifs de la CEDEAO. La Commission, qui a son siège à
                                Abuja, au Nigeria, est dirigée par un Président, assisté d’une Vice-présidente et de
                                cinq Commissaires, tous des professionnels expérimentés qui assurent le leadership en
                                vue de la réalisation de la vision des pères fondateurs, à savoir « une région sans
                                frontières au sein de laquelle les populations ont accès aux ressources abondantes et
                                sont en mesure de les exploiter, grâce à la création d’opportunités dans un
                                environnement durable« .</p>
                            <p>Le Président de la Commission est le Chef de l’Exécutif de la Communauté et de l’ensemble
                                de ses institutions. La Commission exécute les décisions de la Conférence et assure la
                                mise en application des règlements du Conseil des ministres. Elle est également chargée
                                de la promotion des programmes et projets de développement de la Communauté ainsi que
                                des entreprises multinationales de la région, de la convocation, en tant que de besoin,
                                des réunions des ministres sectoriels pour engager la réflexion sur les moyens d’assurer
                                la réalisation des objectifs de la Communauté. La Commission élabore, par ailleurs, les
                                projets de budget et les programmes d’activité de la Communauté et assure la supervision
                                de leur exécution après leur approbation par le Conseil, outre la soumission de rapports
                                sur les activités de la Communauté à toutes les sessions de la Conférence et du Conseil,
                                notamment.</p>
                            <p>
                                La Commission de la CEDEAO veille à la mise en œuvre des programmes stratégiques
                                d’importance cruciale, visant à favoriser le renforcement de la cohésion et
                                l’élimination progressive des obstacles à l’intégration effective de la sous-région.
                                Ainsi, les 300 millions de citoyens de la Communauté pourront finalement s’approprier la
                                réalisation de la nouvelle Vision qui consiste à passer d’une CEDEAO des Etats à une «
                                CEDEAO des Peuples : Paix et prospérité pour tous » à l’horizon 2050.
                            </p>
                            <p>
                                La Commission se compose de départements et de directions. Elle est une institution
                                multilatérale qui emploie des fonctionnaires statutaires, des professionnels et des
                                agents des services généraux, provenant de divers horizons culturels, ethniques,
                                nationaux, professionnels, de genre, intellectuels et professionnels. La Commission est
                                chargée de réaliser la mission, la vision et les objectifs définis dans les protocoles
                                de la CEDEAO, conformément au mandat qui lui est dévolu par le Traité de 1975, révisé en
                                1993.
                            </p>
